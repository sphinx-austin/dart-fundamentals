void main() {
  // LOGIC - If, IF/else, If else if

  var num = 10;
  if (num == 5) {
    print("Number is five!");
  } else {
    print("The number is not five, the number is $num");
  }

  // If else if
  var numbr = 33;
  if (numbr == 10) {
    print("Number is Ten!");
  } else if (numbr == 3) {
    print("The number is three");
  } else {
    print("The number is not ten or three, it is $numbr");
  }
}
